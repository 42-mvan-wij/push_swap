#include "libft.h"
#include <stdlib.h>

/**
 * Calls @p f on each items content of @p lst
 * @param lst
 * @param f
 */
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	while (lst != NULL)
	{
		f(lst->content);
		lst = lst->next;
	}
}

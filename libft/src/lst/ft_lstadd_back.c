#include "libft.h"
#include <stdlib.h>

/**
 * Adds @p new to the end of *@p lst
 * @param lst &(t_list *)lst
 * @param new list to add
 */
void	ft_lstadd_back(t_list **lst, t_list *new)
{
	if (*lst == NULL)
		*lst = new;
	else
		ft_lstlast(*lst)->next = new;
}
